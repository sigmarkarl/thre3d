  void updateCenterOfMass() {
    int len = Corp.corpList.length;
    cx = 0.0;
    cy = 0.0;
    cz = 0.0;
    for (Corp rel in Corp.corpList) {
      cx += rel.getx();
      cy += rel.gety();
      cz += rel.getz();
    }
    if (len > 0) {
      cx /= len;
      cy /= len;
      cz /= len;
    }
    //console( "center update "+len );
  }

